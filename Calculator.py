import math

class Calculator:
    
    def add(a,b):
        return a+b
    
    def sub(a,b):
        return a-b
    
    def multiply(a,b):
        return a*b

    def division(a,b):
        return a/b

    def toThePowerOf(a,b):
        return math.pow(a,b)
